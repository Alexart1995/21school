#include "libft.h"
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	if (n == 0)
		return ;
	while (n > 0)
	{
		str[i] = '\0';
		n--;
		i++;
	}
}
